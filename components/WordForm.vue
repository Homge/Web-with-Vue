<template>
  <form action="#" @submit.prevent="onSubmit">
      <p v-if="errorsPresent" class="error">Please fill out both field!</p>
      <div class="ui labeled input fluid">
          <div class="ui label">
              <i class="spain flag"></i> Spanish
          </div>
          <input type="text" placeholder="Enter word..." v-model="word.spanish"/>
      </div>

      <div class="ui labeled input fluid">
          <div class="ui label">
              <i class="united kingdom flag"></i> English
          </div>
          <input type="text" placeholder="Enter word..." v-model="word.english"/>
      </div>

      <div class="ui labeled input fluid">
          <div class="ui label">
              <i class="germany flag"></i> German
          </div>
          <input type="text" placeholder="Enter word..." v-model="word.german"/>
      </div>
      <button class="positive ui button">Submit</button>
  </form>
</template>

<script>
export default{
  name: 'word-form',
  props:{
      word: {
          type: Object,
          required: false,
          default: () => {
              return {
                  english: '',
                  spanish: '',
                  german: ''
              };
          }
      }
  },
  data() {
      return{
          errorsPresent: false
      }
  },
  methods: {
      onSubmit: function() {
          if (this.word.english ==='' || this.word.spanish === ''|| this.word.german === '') {
              this.errorsPresnt = true;
          } else{
              this.$emit('createOrUpdate', this.word);
          }

      }
  }

};
</script>